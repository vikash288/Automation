package com.pricedotcom.pojo;

public class DQMPrice {


	 double price1;
	 double price2;
	
	public DQMPrice(double price1,double price2){
		this.price1=price1;
		this.price2=price2;
	}
	
	/**
	 * @return the price1
	 */
	public double getPrice1() {
		return price1;
	}

	/**
	 * @return the price2
	 */
	public double getPrice2() {
		return price2;
	}


}
